    <h2>About Us</h2>
    <p>Lagaan Cricket Club is a privately owned Cricket Club played under <a href="http://www.mscl.org/index.php" target="_blank"> Massachusetts Cricket League</a> in 2010 in Boston, Massachusetts.</p>
    <p>Vik Mehrotra, Mahesh Goplan, Sai Vajha, Ramesh Donepudi and Praveen Lakkaraju were the few members who have started the club by taking over the administrative rights from the previous owner Rohit Saini.</p>
	<p>The first baby steps of Lagaan Cricket Club in 2010 season under MSCL started with a bad <a href="http://www.mscl.org/viewScorecard.php?mid=740" target="_blank">loss </a>against Middlesex Cricket club scoring a mere 47 all out.
		 Lagaan Cricket Club has slowly found some momentum in the league with the addition of players like Ryan Hurley, Orien Bostic, Nicholas Standford and has started to see some successes and eventually found a spot in the semi-finals
	  	and then stormed into finals which they have finished with an exciting <a href="http://www.mscl.org/viewScorecard.php?mid=981" target="_blank">win</a> against strong Commonwealth B team inspite of having only 9 players available to field. 
	  	Orien Bostic's heroic 99 and Mahesh Gopalan's resilient performance of unbeaten 37 combined with a 5 wicket haul in the Division II finals secured the championship and promotion to Division I.</p>
	<p>2011 season is completely a different story. The team roster alone scared the opponents with big guns Sharaz Baksh and Timmy Thomas joining the team. 
		Ryan Hurley's devastating spells of genuine off-spins combined with scarily fast bouncers threatened even the top batsmen in the league. 
		Mahesh's accurate spells and Sai's nagging outswingers found it difficult for the opponents while Timmy and Baksh took the opponent attacks with massive hits sending the poor white ball out of the park. 
		The moment the roster was presented, the Division I champioship was guaranteed for Lagaan and rightly so, Lagaan was crowned with Division I Championship.</p>
    <p>Lagaan is now focused on building the club for longevity which can still promote competitive Cricket in North America.</p>
